; прошивка для процессора z80

init:                ; метка начала инициализации
    ld_a 10          ; загрузить в регистр A (аккумулятор) значение 10
    ld_b 20          ; загрузить в регистр B значение 20

loop:                ; метка начала основного цикла
    add_a_b          ; сложить A и B, результат сохранить в A (A = A + B)
    load_frame       ; специальная инструкция: декодировать следующий видео-кадр,
                     ; обновить экран, наложить OSD, увеличить счётчик кадров
    jp loop          ; безусловный переход на метку loop (бесконечный цикл)
